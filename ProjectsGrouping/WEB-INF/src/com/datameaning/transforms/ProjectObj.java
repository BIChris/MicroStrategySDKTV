package com.datameaning.transforms;

public class ProjectObj {

	String id; 

	String name;
	String serverName; 
	int projectStatus;
	String customGroupid;
	String projectDesc;
	
	
	public ProjectObj(String id, String name, String serverName,
			int projectStatus, String customGroupid,
			String projectDesc) {
		super();
		this.id = id;
		this.name = name;
		this.serverName = serverName;
		this.projectStatus = projectStatus;
		this.customGroupid = customGroupid;
		this.projectDesc = projectDesc;
	} 
	
	
	

	
}
